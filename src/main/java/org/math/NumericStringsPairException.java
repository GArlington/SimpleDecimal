package org.math;

public class NumericStringsPairException extends Exception {
	private static final long serialVersionUID = 6375805531945411593L;

	public NumericStringsPairException() {
		super();
	}

	public NumericStringsPairException(String message, Throwable cause,
	                                   boolean enableSuppression,
	                                   boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public NumericStringsPairException(String message, Throwable cause) {
		super(message, cause);
	}

	public NumericStringsPairException(String message) {
		super(message);
	}

	public NumericStringsPairException(Throwable cause) {
		super(cause);
	}
}
